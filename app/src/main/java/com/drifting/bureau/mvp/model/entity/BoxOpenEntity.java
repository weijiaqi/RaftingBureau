package com.drifting.bureau.mvp.model.entity;

public class BoxOpenEntity {

        private String name;
        private Integer space_times;
        private Integer status;

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public Integer getSpace_times() {
            return space_times;
        }

        public void setSpace_times(Integer space_times) {
            this.space_times = space_times;
        }

        public Integer getStatus() {
            return status;
        }

        public void setStatus(Integer status) {
            this.status = status;
        }

}
